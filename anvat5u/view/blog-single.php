            hòa quyện với hương của lá chanh, của ớt, .. Sự hòa quyện tuyệt vời ấy tạo nên một hương vị đặc biệt, một
            hương vị như quen thuộc lại ngỡ như xa lạ. Nhiều thực khách ví von nó như sự thăng hoa của vị giác và như sự
            thư giãn của tinh thần. Còn khi ăn bạn chỉ cần thưởng thức 1 sợi thịt gà nhỏ thôi cũng đủ để cảm nhận cái vị
            đặc biệt của nó .. vị ngọt thơm của thịt gà, vị cay cay của ớt, một chút đắng một chút thơm từ lá chanh đảm
