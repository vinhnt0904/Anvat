          <h4 class="mb-3">Các bước làm món khô gà lá chanh</h4>
          <p>Bước 1: Sơ chế và luộc thịt gà</p>
          <p>Làm sạch thịt gà bằng nước muối pha loãng, để kỹ càng hơn có thể dùng rượu trắng để khử mùi tanh của thịt
            sau đó rửa lại với nước sạch.</p>
          <p><img src="images/cacbuoclamga.jpg" alt="" class="img-fluid"></p>
          <p>Cho thịt gà vào nồi luộc đổ nước ngập phần thịt gà, thêm vào nồi 1 muỗng cà phê muối hột, 1 ít lá chanh,
            gừng thái lát và sả. Luộc thịt trong 20 phút rồi tắt bếp, giữ nguyên nắp nồi trong khoảng 10 phút để thịt gà
            ngấm và thơm hơn.</p>
          <p>Bước 2: Ướp thịt gà đã xé</p>
          <p>Để thịt gà không bị thâm và nhanh nguội thì rửa qua nước đá khi vừa vớt ra, bỏ thịt gà ra đĩa khi nguội hẳn
            xé phần thịt gà thành từng miếng vừa ăn cỡ nữa ngón tay, không nên xé quá nhỏ vì khi làm thịt gà khô sẽ bị
            nát.
            Lá chanh thái thành từng sợi nhỏ, tỏi và sả băm nhuyễn rồi cho hỗn hợp này vào phần thịt gà xé trộn thật đều
            tay.
            Tiếp tục thêm vào nước mắm, ớt bột, ngũ vị hương, đường vào trộn thật đều một lần nữa ướp trong 30 phút để
            ngấm gia vị.</p>
          <p><img src="images/cacbuoclamga2.png" alt=""></p>
          <p>Bước 3: Hoàn thiện</p>
          <p><img src="images/kho-ga-la-chanh-632.jpg" alt="" class="img-fluid"></p>
          <p>Dùng một muỗng dầu ăn quét đều quanh nồi cơm điện.</p>
          <p>Cho phần thịt gà sợi vừa ướp bên trên vào nồi sau đó bỏ thêm một ít lá chanh cùng với phần ớt thái lát vào
            trộn đều tay và dàn đều hỗn hợp trong nồi cơm điện để khi sấy phần thịt sẽ khô đều nhau hơn.</p>
          <p>Bật chế độ “cook” của nồi cơm điện để đun sôi gà và làm bay hơi nước giúp phần thịt gà săn và khô lại.</p>
          <p>Khi nồi chuyển qua chế “warm” để vậy khoảng 5 phút rồi tiếp tục nhấn “cook” để sấy thịt gà khô. Giai đoạn
            này được lặp lại khoảng 8 đến 9 lần (nằm trong khoảng 40 đến 45 phút) làm thịt gà khô hẳn đi để có được món
            thịt gà khô lá chanh.</p>
